# Algorithm reader and writer method with counter.


def remove_duplicates_for_more_than_2(nums):
    """
    :type nums: List[int]
    :rtype: int
    """
    writer = 1
    counter = 0
    for i in range(1, len(nums)):
        if nums[i] == nums[i - 1]:
            counter += 1
        else:
            counter = 0

        if counter <= 1:
            nums[writer] = nums[i]
            writer += 1

    return writer
